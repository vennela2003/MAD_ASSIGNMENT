public class Image {
    private String id;
    private String download_url;

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDownloadUrl() {
        return download_url;
    }

    public void setDownloadUrl(String downloadUrl) {
        this.download_url = downloadUrl;
    }
}
